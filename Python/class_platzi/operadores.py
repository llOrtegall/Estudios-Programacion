# TODO: Operadores en Python
a = 3; b = 2

# Operador de suma
suma = a + b
print('Suma:', suma)

# Operador de resta
resta = a - b
print('Resta:', resta)

# Operador de multiplicación
multiplicacion = a * b
print('Multiplicación:', multiplicacion)

# Operador de división
division = a / b
print('División:', division)

# Operador de módulo
modulo = a % b
print('Módulo:', modulo)
